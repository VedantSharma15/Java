public class Assign2
{
	public static void main(String[] args)
	{
		byte a=25;
		char b='x';
		int c=125;
		double d=123456D;
		long e=11111L;
		float f=123.456F;
		short g=8;
		boolean h=false;
		System.out.println("Byte data= "+a);
		System.out.println("Char data= "+b);
		System.out.println("Int data= "+c);
		System.out.println("Double data= "+d);
		System.out.println("Long data= "+e);
		System.out.println("Float data= "+f);
		System.out.println("Short data= "+g);
		System.out.println("Boolean data= "+h);
	}
}